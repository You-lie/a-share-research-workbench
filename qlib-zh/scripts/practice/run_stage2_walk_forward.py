#!/usr/bin/env python3
"""Walk-forward stage2 runner for Alpha158 practice.

This script operates in three layers:

1. Trading-calendar driven walk-forward fold generation.
     - Folds can be generated by stepping backward yearly or forward weekly.
     - For each fold anchor t the split is strictly causal (no leakage):
         train = [t - (train_years + valid_years),  t - valid_years)  ← 6 y
         dev   = [t - valid_years,                  t)                ← 1 y
         test  = [t,                                t + test_years)    ← 1 y
     - The model is trained only on train/dev data and frozen before test.
     - A weekly mode (step_years=0, step_weeks>0) is also supported.

2. Segmented execution with fold-skip / resume support.
     - Folds are grouped by year chunks so the pipeline can be paused and
       resumed without re-running completed folds.
     - A fold is skipped if its output scores.csv already exists.

3. Full-cycle backtest aggregation.
     - All fold-level test-period predictions (all_scores.csv) are
       concatenated into a continuous signal series.
    - A live-aligned TopK walk-forward backtest generates a consolidated report:
       net-value curve, turnover, IC/ICIR, Sharpe, max drawdown, and
       a human-readable report_of_backtest.txt.

The latest fold outputs are copied to the root output dir so the existing
stage3/4/5/6 pipeline remains unchanged.

The implementation also clips the requested walk-forward end date to the
latest date available in the local qlib calendar and reports signal-coverage
diagnostics, which is important when annual folds only contribute short OOS
test windows.
"""
