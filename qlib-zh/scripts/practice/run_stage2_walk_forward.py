def _precompute_handler_cache(
    cache_path: str,
    template_cfg: dict,
    start_time: str,
    end_time: str,
    provider_uri: str = "",
    workers: int = 1,
) -> None:
    """Precompute Alpha158 features for the full date range and save to disk (pickle).

    Must be called after qlib.init() (i.e. after _load_trade_calendar).

    When ``workers > 1``, instruments are split across parallel subprocesses,
    each handling a chunk of stocks independently.  This is 3-4× faster on
    multi-core machines for the initial cache build.

    This is inlined from ``scripts.small.cached_handler`` to avoid import failures
    when this module is loaded via ``importlib.spec_from_file_location()`` (the
    ``run_stage2_walk_forward_small.py`` wrapper), which doesn't put the repo root
    on ``sys.path``.
    """
    import copy
    import os as _os

    from qlib.data.dataset.handler import DataHandlerLP
    from qlib.utils import init_instance_by_config

    try:
        handler_cfg = template_cfg["task"]["dataset"]["kwargs"]["handler"]
    except KeyError:
        raise RuntimeError("Cannot find handler config in template")

    # Override instruments from TARGET_MARKET env var (respects DEBUG / custom markets)
    target_market = _os.environ.get("TARGET_MARKET", "")
    if target_market:
        handler_cfg = copy.deepcopy(handler_cfg)
        handler_cfg.setdefault("kwargs", {})["instruments"] = target_market

    if workers > 1:
        from scripts.small.cached_handler import _precompute_handler_cache_parallel
        _precompute_handler_cache_parallel(
            cache_path, template_cfg, handler_cfg,
            start_time, end_time, provider_uri, workers,
        )
        return

    # ── Serial path (original) ──────────────────────────────────
    from scripts.small.cached_handler import _dump_cached_data
    handler_cfg = copy.deepcopy(handler_cfg)
    handler_cfg["kwargs"]["start_time"] = start_time
    handler_cfg["kwargs"]["end_time"] = end_time
    handler_cfg["kwargs"]["fit_start_time"] = start_time
    handler_cfg["kwargs"]["fit_end_time"] = end_time

    handler = init_instance_by_config(handler_cfg)
    handler.setup_data(DataHandlerLP.IT_FIT_SEQ)

    if handler._data is None or handler._data.empty:
        raise RuntimeError("Handler produced empty _data — cannot cache")

    _dump_cached_data(handler._data, cache_path)
    n_rows = len(handler._data)
    n_dates = handler._data.index.get_level_values("datetime").nunique()
    n_inst = handler._data.index.get_level_values("instrument").nunique()
    print(f"  [Cache] Saved {n_rows} rows ({n_dates} dates × {n_inst} inst) → {cache_path}")




def _train_single_model_fold(
    spec: dict[str, object],
    fold: dict[str, str],
    fold_dir: Path,
    mlruns_root: Path,
    base_env: dict[str, str],
    uri_folder: str,
    experiment_name: str,
    half_life: int,
    floor: float,
    train_base_start: str,
    warm_start_checkpoint: Path | None = None,
    handler_cache: str | None = None,
) -> tuple[Path, str]:
    model_name, model_dir, fold_config = _generate_model_fold_config(
        spec=spec,
        fold=fold,
        fold_dir=fold_dir,
        base_env=base_env,
        half_life=half_life,
        train_base_start=train_base_start,
        handler_cache=handler_cache,
    )

    existing_run_dir = model_dir / "mlflow_run"
    existing_artifacts = existing_run_dir / "artifacts"
    required_artifacts = [
        existing_artifacts / "pred.pkl",
        existing_artifacts / "label.pkl",
        existing_artifacts / "valid_pred.pkl",
        existing_artifacts / "valid_label.pkl",
        existing_artifacts / "params.pkl",
    ]
    if all(path.exists() for path in required_artifacts):
        print(f"      reuse cached run: {model_name} -> {existing_run_dir}")
        return existing_run_dir, f"{experiment_name}_wf_{fold['signal_date']}_{model_name}"

    experiment_tag = f"{experiment_name}_wf_{fold['signal_date']}_{model_name}"
    start_ts = time.time()
    if warm_start_checkpoint is not None:
        print(f"      warm-start: {model_name} <- {warm_start_checkpoint}")
    else:
        print(f"      warm-start: {model_name} <- <none>")
    _run(
        [
            sys.executable,
            str(SCRIPTS / "run_stage2_practice.py"),
            "--config",
            str(fold_config),
            "--experiment-name",
            experiment_tag,
            "--uri-folder",
            uri_folder,
            "--half-life",
            str(half_life),
            "--floor",
            str(floor),
            *(["--warm-start", str(warm_start_checkpoint)] if warm_start_checkpoint is not None else []),
        ],
        cwd=ROOT,
        env=base_env,
    )

    run_dir = _find_latest_run_dir(
        mlruns_root=mlruns_root,
        experiment_name=experiment_tag,
        start_ts=start_ts,
    )
    model_run_dir = model_dir / "mlflow_run"
    if model_run_dir.exists():
        shutil.rmtree(model_run_dir)
    shutil.copytree(run_dir, model_run_dir)
    try:
        shutil.rmtree(run_dir)
    except Exception:
        pass
    return model_run_dir, experiment_tag


def _build_ensemble_fold_output(
    fold: dict[str, str],
    fold_dir: Path,
    fold_output: Path,
    mlruns_root: Path,
    template_cfg: dict,
    base_env: dict[str, str],
    args: argparse.Namespace,
    previous_checkpoints: dict[str, Path],
) -> dict[str, object]:
    model_artifacts: dict[str, Path] = {}
    validation_ics: dict[str, float] = {}
    test_preds: dict[str, pd.DataFrame] = {}
    label_frames: dict[str, pd.DataFrame] = {}

    print(f"  → sequential base models: {', '.join(str(spec['name']) for spec in MODEL_SPECS)}")
    for spec in MODEL_SPECS:
        model_name = str(spec["name"])
        print(f"    · training {model_name}")
        model_run_dir, _ = _train_single_model_fold(
            spec=spec,
            fold=fold,
            fold_dir=fold_dir,
            mlruns_root=mlruns_root,
            base_env=base_env,
            uri_folder=args.uri_folder,
            experiment_name=args.experiment_name,
            half_life=args.half_life,
            floor=args.floor,
            train_base_start=args.train_base_start,
            warm_start_checkpoint=previous_checkpoints.get(model_name),
            handler_cache=getattr(args, "handler_cache", None),
        )
        model_artifacts[model_name] = model_run_dir
        artifacts = model_run_dir / "artifacts"

        valid_pred = _to_score_frame(_load_pickle(artifacts / "valid_pred.pkl"), "score")
        valid_label = _to_label_frame(_load_pickle(artifacts / "valid_label.pkl"))
        _, ic_mean, _ = _compute_daily_ic(valid_pred, valid_label)
        validation_ics[model_name] = ic_mean

        test_preds[model_name] = _to_score_frame(_load_pickle(artifacts / "pred.pkl"), "score")
        label_frames[model_name] = _to_label_frame(_load_pickle(artifacts / "label.pkl"))

    target_names = _target_route_model_names()
    label_df = None
    for model_name in target_names:
        if model_name in label_frames:
            label_df = label_frames[model_name]
            break
    if label_df is None and label_frames:
        label_df = next(iter(label_frames.values()))

    if label_df is None:
        raise RuntimeError("No test label data found for ensemble fold")

    weights = _compute_ensemble_weights(validation_ics)
    print(f"  → validation IC weights: {weights}")
    ensemble_pred = _combine_model_predictions(test_preds, weights)
    strategy_metrics = _compute_strategy_metrics(ensemble_pred[["score"]], label_df, args.hold_num)

    ensemble_run_dir = fold_dir / "ensemble_run"
    if ensemble_run_dir.exists():
        shutil.rmtree(ensemble_run_dir)
    _build_synthetic_run_dir(
        ensemble_run_dir=ensemble_run_dir,
        pred_df=ensemble_pred[["score"]],
        label_df=label_df[["label"]],
        validation_ics=validation_ics,
        weights=weights,
    )
    _export_fold_outputs(ensemble_run_dir, fold_output, fold["pred_date"], base_env)
    fold_metrics = _augment_exported_scores(
        fold_output=fold_output,
        pred_date=fold["pred_date"],
        weights=weights,
        validation_ics=validation_ics,
        strategy_metrics=strategy_metrics,
    )

    (fold_output / "ensemble_metadata.json").write_text(
        json.dumps(
            {
                "pred_date": fold["pred_date"],
                "validation_ic": validation_ics,
                "weights": weights,
                "strategy_metrics": strategy_metrics,
                "models": [spec["name"] for spec in MODEL_SPECS],
                "warm_start_strategy": WARM_START_STRATEGY,
            },
            ensure_ascii=False,
            indent=2,
        ),
        encoding="utf-8",
    )
    return fold_metrics


