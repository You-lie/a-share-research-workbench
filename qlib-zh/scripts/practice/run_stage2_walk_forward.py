def _to_score_frame(obj, score_col: str = "score") -> pd.DataFrame:
    if isinstance(obj, pd.Series):
        df = obj.to_frame(score_col)
    elif isinstance(obj, pd.DataFrame):
        df = obj.copy()
        if score_col not in df.columns:
            first = df.columns[0]
            df = df[[first]].rename(columns={first: score_col})
        else:
            df = df[[score_col]]
    else:
        raise TypeError(f"Unsupported score object type: {type(obj)!r}")

    if isinstance(df.index, pd.MultiIndex):
        names = list(df.index.names)
        if len(names) >= 2:
            names[0] = "datetime"
            names[1] = "instrument"
            df.index = df.index.set_names(names)
    return df.sort_index()


def _to_label_frame(obj) -> pd.DataFrame:
    if isinstance(obj, pd.Series):
        df = obj.to_frame("label")
    elif isinstance(obj, pd.DataFrame):
        df = obj.copy()
        if "label" not in df.columns:
            first = df.columns[0]
            df = df[[first]].rename(columns={first: "label"})
        else:
            df = df[["label"]]
    else:
        raise TypeError(f"Unsupported label object type: {type(obj)!r}")

    if isinstance(df.index, pd.MultiIndex):
        names = list(df.index.names)
        if len(names) >= 2:
            names[0] = "datetime"
            names[1] = "instrument"
            df.index = df.index.set_names(names)
    return df.sort_index()


def _daily_rank_gaussian(score_df: pd.DataFrame, score_col: str) -> pd.DataFrame:
    out = score_df.copy()

    def _scale(group: pd.DataFrame) -> pd.DataFrame:
        s = pd.to_numeric(group[score_col], errors="coerce")
        if s.notna().sum() <= 1:
            group[score_col] = 0.0
            return group
        pct = s.rank(pct=True, method="average").clip(1e-6, 1 - 1e-6)
        group[score_col] = (pct - 0.5) * 2.0
        return group

    return out.groupby(level="datetime", group_keys=False).apply(_scale)


def _compute_daily_ic(pred_df: pd.DataFrame, label_df: pd.DataFrame) -> tuple[pd.Series, float, float]:
    joined = pred_df.join(label_df, how="inner").dropna()
    if joined.empty:
        return pd.Series(dtype=float), float("nan"), float("nan")

    ic_series = joined.groupby(level="datetime", group_keys=False).apply(
        lambda x: x["label"].corr(x["score"])
    ).dropna()
    ic_mean = float(ic_series.mean()) if not ic_series.empty else float("nan")
    ic_std = float(ic_series.std(ddof=1)) if len(ic_series) > 1 else float("nan")
    icir = ic_mean / ic_std if pd.notna(ic_std) and ic_std not in (0, 0.0) else float("nan")
    return ic_series, ic_mean, icir


def _compute_strategy_metrics(pred_df: pd.DataFrame, label_df: pd.DataFrame, hold_num: int) -> dict[str, float]:
    """Compute fold-level strategy metrics using buffered equal-weight simulation.

    Uses the same dropout buffer logic as the full backtest (_build_buffered_equal_weight_signal),
    so fold-level metrics are consistent with the full backtest report.
    """
    joined = pred_df.join(label_df, how="inner").dropna()
    if joined.empty:
        return {
            "annualized_return": float("nan"),
            "max_drawdown": float("nan"),
            "sharpe_ratio": float("nan"),
            "monthly_win_rate": float("nan"),
            "IC_mean": float("nan"),
            "ICIR": float("nan"),
            "win_rate": float("nan"),
        }

    daily_ic, ic_mean, icir = _compute_daily_ic(pred_df, label_df)
    dropout_buffer_pct = float(os.environ.get("FULL_BACKTEST_BUFFER_PCT", "0.5") or 0.5)

    # Stateful buffered selection (same logic as _build_buffered_equal_weight_signal)
    prev_holdings: set[str] = set()
    daily_rets: list[float] = []
    dates: list[pd.Timestamp] = []

    for dt, grp in (
        joined.reset_index()
        .sort_values(["datetime", "score"], ascending=[True, False], na_position="last")
        .groupby("datetime", sort=True)
    ):
        if grp.empty:
            continue
        n = len(grp)
        keep_rank_cutoff = max(int(hold_num), int(np.ceil(n * max(float(dropout_buffer_pct), 0.0))))
        grp = grp.reset_index(drop=True)
        grp["rank"] = np.arange(1, n + 1)

        rank_map = grp.set_index("instrument")["rank"].to_dict()
        kept = [code for code in prev_holdings if code in rank_map and int(rank_map[code]) <= keep_rank_cutoff]
        additions = [code for code in grp["instrument"].tolist() if code not in kept]
        target_codes = (kept + additions)[:hold_num]
        if not target_codes:
            prev_holdings = set()
            continue

        selected = grp[grp["instrument"].isin(target_codes)]
        port_ret = selected["label"].mean()
        daily_rets.append(float(port_ret))
        dates.append(pd.Timestamp(dt))
        prev_holdings = set(target_codes)

    daily_ret = pd.Series(daily_rets, index=pd.to_datetime(dates))
    if daily_ret.empty:
        ann_ret = mdd = sharpe = monthly_win = win_rate = float("nan")
    else:
        ann_ret = float((1 + daily_ret).prod() ** (252 / max(len(daily_ret), 1)) - 1)
        equity = (1 + daily_ret).cumprod()
        mdd = float((equity / equity.cummax() - 1).min())
        std = float(daily_ret.std(ddof=1)) if len(daily_ret) > 1 else float("nan")
        sharpe = float(daily_ret.mean() / std * np.sqrt(252)) if pd.notna(std) and std > 0 else float("nan")
        monthly = (1 + daily_ret).resample("ME").prod() - 1
        monthly_win = float((monthly > 0).mean()) if not monthly.empty else float("nan")
        win_rate = float((daily_ret > 0).mean()) if not daily_ret.empty else float("nan")

    return {
        "annualized_return": ann_ret,
        "max_drawdown": mdd,
        "sharpe_ratio": sharpe,
        "monthly_win_rate": monthly_win,
        "IC_mean": ic_mean,
        "ICIR": icir,
        "win_rate": win_rate,
        "ic_days": float(len(daily_ic)),
    }


def _compute_ensemble_weights(validation_ics: dict[str, float]) -> dict[str, float]:
    ordered_names = [str(spec["name"]) for spec in MODEL_SPECS]
    route_map = {name: str((_model_spec_by_name(name) or {}).get("route", "default")) for name in ordered_names}
    raw_map = {}
    for name in ordered_names:
        val = float(validation_ics.get(name, np.nan))
        raw_map[name] = max(val, 0.0) if np.isfinite(val) else 0.0

    unique_routes = list(dict.fromkeys(route_map.values()))
    if len(unique_routes) <= 1:
        raw = np.array([raw_map[name] for name in ordered_names], dtype=float)
        total = float(raw.sum())
        if total <= 1e-12:
            weights = np.repeat(1.0 / max(len(ordered_names), 1), len(ordered_names))
        else:
            weights = raw / total
        return {name: float(weight) for name, weight in zip(ordered_names, weights)}

    route_strength = {}
    for route in unique_routes:
        route_names = [name for name in ordered_names if route_map[name] == route]
        route_raw = np.array([raw_map[name] for name in route_names], dtype=float)
        route_strength[route] = float(route_raw.mean()) if route_raw.size else 0.0

    route_total = float(sum(route_strength.values()))
    if route_total <= 1e-12:
        route_weights = {route: 1.0 / max(len(unique_routes), 1) for route in unique_routes}
    else:
        route_weights = {route: strength / route_total for route, strength in route_strength.items()}

    weights: dict[str, float] = {}
    for route in unique_routes:
        route_names = [name for name in ordered_names if route_map[name] == route]
        route_raw = np.array([raw_map[name] for name in route_names], dtype=float)
        within_total = float(route_raw.sum())
        if within_total <= 1e-12:
            within_weights = np.repeat(1.0 / max(len(route_names), 1), len(route_names))
        else:
            within_weights = route_raw / within_total
        for name, within_weight in zip(route_names, within_weights):
            weights[name] = float(route_weights[route] * within_weight)

    final_total = float(sum(weights.values()))
    if final_total > 0:
        weights = {name: float(weight / final_total) for name, weight in weights.items()}
    return weights


def _combine_model_predictions(model_frames: dict[str, pd.DataFrame], weights: dict[str, float]) -> pd.DataFrame:
    combined = None
    target_index = None
    target_names = set(_target_route_model_names())
    for name, frame in model_frames.items():
        score_df = _to_score_frame(frame, score_col="score")
        if name in target_names:
            target_index = score_df.index if target_index is None else target_index.intersection(score_df.index)
        score_df = _daily_rank_gaussian(score_df, "score").rename(columns={"score": f"score_{name}"})
        if combined is None:
            combined = score_df
        else:
            combined = combined.join(score_df, how="outer")

    if combined is None or combined.empty:
        raise RuntimeError("No model predictions available for ensemble")

    if target_index is not None and len(target_index) > 0:
        combined = combined.reindex(target_index)

    score_cols = []
    combined = combined.sort_index()
    for name in weights:
        col = f"score_{name}"
        if col in combined.columns:
            combined[col] = pd.to_numeric(combined[col], errors="coerce").fillna(0.0)
            score_cols.append(col)

    combined["score"] = 0.0
    for name, weight in weights.items():
        col = f"score_{name}"
        if col in combined.columns:
            combined["score"] = combined["score"] + combined[col] * float(weight)

    keep_cols = [*score_cols, "score"]
    return combined[keep_cols]


